sub valley {
  my( $L, $R, @d )=( 0, 0, slide { $b <=> $a } @_ );
  for my $l ( 0 .. $#d-1) {
    my($x,$t) = ( $d[$l] || -1, $l );
    while($t++<$#d) {
      $d[$t] && ( $d[$t]<$x ? last : ($x=$d[$t]) )
    }
    ($L,$R)=($l,$t) if $R-$L<$t-$l;
  }
  @_[$L..$R];
}

sub valley2 {
  my( $pd, $s, $s2, $S, $E, $d ) = ( 0, 0, 0, 0, 0 );
  for( 0 .. $#_ ) {
    $d  = $_[$_]-$_[$_-1];
    $s2 = $_-1                                                if $pd > 0 && $d  <= 0; ## Start of plateau
    ( $_-$s > $E-$S ) && ( ( $S, $E, $s ) = ( $s, $_, $s2 ) ) if $d  < 0 && $pd >= 0; ## End of plateau
    $pd = $d
  }
  @_-$s2 > $E-$S ? @_[ $s2 .. $#_ ] : @_[ $S .. $E-1 ]        ## Check the last valley...
}

