sub f {
    my($ip, $nw) = @_;
    my $n = join'|',0..255;
    my $m = join'|',0..32;
    my $re_ip = qr{^($n)\.($n)\.($n)\.($n)$};
    my $re_nw = qr{^($n)\.($n)\.($n)\.($n)/($m)$};

    $ip =~ $re_ip or return 0; $ip = $1 * 256**3  +  $2 * 256**2  +  $3 * 256  +  $4;
    $nw =~ $re_nw or return 0; $nw = $1 * 256**3  +  $2 * 256**2  +  $3 * 256  +  $4;

    my $mask = 2 ** 32  -  2 ** (32 - $5);

    my $nw_from = ($nw & $mask);
    my $nw_to   = ($nw & $mask)  +  2 ** (32 - $5) - 1;

    $nw_from <= $ip <= $nw_to ? 1: 0;
}

print pop(@$_) == f(@$_) ? "ok\n" : "ERROR\n" for
