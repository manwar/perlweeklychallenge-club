use List::Util qw(max sum);

sub f {
    max map sum( @$_ ), @_
}

print pop(@$_) == f(@$_) ? "ok\n" : "error\n" for 

[ [4,  4, 4, 4],
  [10, 0, 0, 0],
  [2,  2, 2, 9]     => 16 ],

[ [1, 5],
  [7, 3],
  [3, 5]            => 10 ],

[ [1, 2, 3],
  [3, 2, 1]         => 6 ],

[ [2, 8, 7],
  [7, 1, 3],
  [1, 9, 5]         => 17 ],

[ [10, 20,  30],
  [5,  5,   5],
