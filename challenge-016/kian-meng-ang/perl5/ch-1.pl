#!/usr/bin/env perl
# vi:et:sw=4 ts=4 ft=perl

use strict;
use warnings;
use utf8;
use feature qw(say);

my ($p, $s, $ls, $ng) = (100, 0, 0, 0);
my $fmt = '%-3s %-3s %-10s %-20s %-10s';

say sprintf $fmt, 'Nth', '%', 'Graph', 'Slice', 'Pie Left';

foreach my $g (1..100) {
    $s = $p * $g / 100;
    $p -= $s;
    if ($s > $ls) { $ls = $s; $ng = $g; }
    say sprintf $fmt, $g, $g, '.' x int($s), $s, $p;
}

say "\nGuest no $ng gets the largest piece ($ls) of pie.";

1;

