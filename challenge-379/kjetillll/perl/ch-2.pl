sub f {
    my($base, $limit) = @_;
    0, grep {
        my $n = $_ * $base;
        my $l = int log($n) / log($base);
        $_ == eval join '+', map { ( ( $n = int( $n / $base ) ) % $base ) ** $l } 1 .. $l
    }
    1 .. $limit
}

