sub f {
    my($a, $b, $c ,$d) = ( shift =~ /(.)(.):(.)(.)/ );
      ( $a.$b eq '??'           ? 24
      : $a    eq '?'  && $b < 4 ?  3
      : $a    eq '?'            ?  2
      : $b    eq '?'  && $a < 2 ? 10
      : $b    eq '?'            ?  4
      :                            1
      )
    * ( $c eq '?'               ?  6 : 1 )
    * ( $d eq '?'               ? 10 : 1 )
}


print f( $$_[0] ) == $$_[1] ? "ok\n" : "ERROR\n" for
[ "?2:34" =>    3 ],
[ "?4:?0" =>   12 ],
[ "??:??" => 1440 ],
[ "?3:45" =>    3 ],
[ "2?:15" =>    4 ],
[ "2?:1?" =>   40 ],
[ "2?:?7" =>   24 ],
[ "1?:?7" =>   60 ],
[ "1?:5?" =>  100 ]
